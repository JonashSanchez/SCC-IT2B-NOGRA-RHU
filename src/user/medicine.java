    private void jPanel4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel4MouseClicked
   
  String name = mname.getSelectedItem().toString();
String desc = mdesc.getSelectedItem().toString();
String qtyStr = mqty.getText().trim();
String expiryInput = mexpiry.getText().trim();

SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
sdf.setLenient(false);
java.util.Date expiryDate;
String expiry;

try {
    expiryDate = sdf.parse(expiryInput);

    
    java.util.Date today = new java.util.Date();
    if (expiryDate.before(sdf.parse(sdf.format(today)))) {
        JOptionPane.showMessageDialog(null, "Expiry date cannot be in the past!", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }

    expiry = sdf.format(expiryDate); 
} catch (ParseException e) {
    JOptionPane.showMessageDialog(null, "Please enter a valid expiry date (yyyy-MM-dd)!", "Warning", JOptionPane.WARNING_MESSAGE);
    return;
}

int qty;
try {
    qty = Integer.parseInt(qtyStr);
    if (qty < 0) throw new NumberFormatException();
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(null, "Quantity must be a valid non-negative number!", "Warning", JOptionPane.WARNING_MESSAGE);
    return;
}

dbConnect dbc = new dbConnect();
String sql = "INSERT INTO tbl_medicines (m_name, m_desc, m_qty, m_expiry) VALUES (?, ?, ?, ?)";

try (PreparedStatement pst = dbc.connect.prepareStatement(sql)) {
    pst.setString(1, name);
    pst.setString(2, desc);
    pst.setInt(3, qty);
    pst.setString(4, expiry);

    if (pst.executeUpdate() > 0) {
        Session sess = Session.getInstance();

String logQuery = "INSERT INTO tbl_logs (u_id, name, action_time, log_action) " +
                  "VALUES (" + sess.getUid() + ", '" +
                  sess.getFname() + " " + sess.getLname() + "', NOW(), 'Admin added a medicine')";
        JOptionPane.showMessageDialog(null, "Medicine successfully added!");
    }
} catch (SQLException e) {
    JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
}



    }//GEN-LAST:event_jPanel4MouseClicked

