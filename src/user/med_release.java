    private void jLabel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MouseClicked
     String appointmentId = appoid.getText().trim();
    String medicineId = medid.getText().trim();
    String qtyText = qtyrelease.getText().trim();
    String date = daterelease.getText().trim();

    // Check for empty fields
    if (appointmentId.isEmpty() || medicineId.isEmpty() || qtyText.isEmpty() || date.isEmpty()) {
        JOptionPane.showMessageDialog(null, "All fields must be filled!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Validate date
    java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
    sdf.setLenient(false);
    java.util.Date selectedDate;
    try {
        selectedDate = sdf.parse(date);
        java.util.Date today = new java.util.Date();
       if (selectedDate.before(sdf.parse(sdf.format(today)))) {

            JOptionPane.showMessageDialog(null, "Released date must be today or in the future!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    } catch (java.text.ParseException e) {
        JOptionPane.showMessageDialog(null, "Invalid date format! Please use YYYY-MM-DD.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        int aId = Integer.parseInt(appointmentId);
        int mId = Integer.parseInt(medicineId);
        int qty = Integer.parseInt(qtyText);

        dbConnect dbc = new dbConnect();

        // Get current quantity from tbl_medicines
        ResultSet rs = dbc.getData("SELECT m_qty FROM tbl_medicines WHERE m_id = " + mId);
        if (rs.next()) {
            int currentQty = rs.getInt("m_qty");
            if (qty > currentQty) {
                JOptionPane.showMessageDialog(null, "Insufficient stock! Available quantity: " + currentQty, "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Insert into tbl_medicine_release
            String insert = "INSERT INTO tbl_medicine_release (a_id, m_id, qty_released, released_date) VALUES (?, ?, ?, ?)";
            java.sql.PreparedStatement pstInsert = dbc.connect.prepareStatement(insert);
            pstInsert.setInt(1, aId);
            pstInsert.setInt(2, mId);
            pstInsert.setInt(3, qty);
            pstInsert.setString(4, date);
            pstInsert.executeUpdate();

            // Update tbl_medicines
            String update = "UPDATE tbl_medicines SET m_qty = m_qty - ? WHERE m_id = ?";
            java.sql.PreparedStatement pstUpdate = dbc.connect.prepareStatement(update);
            pstUpdate.setInt(1, qty);
            pstUpdate.setInt(2, mId);
            pstUpdate.executeUpdate();
            
            
           Session sess = Session.getInstance();

String logQuery = "INSERT INTO tbl_logs (u_id, name, action_time, log_action) " +
                  "VALUES (" + sess.getUid() + ", '" +
                  sess.getFname() + " " + sess.getLname() + "', NOW(), 'Admin released a medicine')";

            JOptionPane.showMessageDialog(null, "Medicine release recorded and stock updated.");
        } else {
            JOptionPane.showMessageDialog(null, "Medicine not found!", "Error", JOptionPane.ERROR_MESSAGE);
        }

        rs.close();
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(null, "Quantity, appointment ID, and medicine ID must be numeric!", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jLabel3MouseClicked

