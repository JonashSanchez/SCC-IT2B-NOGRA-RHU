    private void displayReport() {
        try {
            dbConnect dbc = new dbConnect();
    String query = 
    "SELECT " +
    "u.u_id AS 'User ID', " +
    "u.u_fname AS 'First Name' " +
    "FROM users u " +
    "WHERE u.u_type = 'Patient' " +
    "ORDER BY u.u_fname ASC";


            ResultSet rs = dbc.getData(query);
            reporttable.setModel(DbUtils.resultSetToTableModel(rs));
            rs.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error loading report: " + ex.getMessage());
        }
    }
